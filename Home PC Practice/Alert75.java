// Bitwise Operator: &, |, ^
// X-OR Operator returns true iff both arguments are different
class Alert75
{
	static public void main(String... kuntan)
	{
		System.out.println(true^false);
		System.out.println(false^true);
		System.out.println(4&5);
		System.out.println(4|5);
		System.out.println(4^5);
	}
}
