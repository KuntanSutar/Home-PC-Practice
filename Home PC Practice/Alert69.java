// Important results about NaN (Not a Number)
class Alert69
{
	public static void main(String[] args)
	{
		float x=10;
		System.out.println(x<Float.NaN);
		System.out.println(x<=Float.NaN);
		System.out.println(x>Float.NaN);
		System.out.println(x>=Float.NaN);
		System.out.println(x==Float.NaN);
		System.out.println(Float.NaN==Float.NaN);
		System.out.println(x!=Float.NaN);
		System.out.println(Float.NaN!=Float.NaN);
	}
}