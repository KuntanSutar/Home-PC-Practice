class Alert71
{
	public static void main(String[] args)
	{
		System.out.println(true>false);				// Can't be applied to boolean types
		System.out.println("Kuntan">"Prasad");		// Can't be applied to Strings (Objects)
		System.out.println(10>20>30);				// Nesting Not allowed
	}
}