// Implicit Type Casting
class Alert77
{
	static public void main(String... kuntan)
	{
		int x='a';
		double d=10;
		System.out.println(x);
		System.out.println(d);
		int y=130;
		byte b=(byte)y;
		System.out.println(b);
		int z=150;
		short s=(short)z;
		System.out.println(s);
		byte c=(byte)z;
		System.out.println(c);
		double m=130.456;
		int n=(int)m;
		System.out.println(n);
		byte k=(byte)m;
		System.out.println(k);
	}
}